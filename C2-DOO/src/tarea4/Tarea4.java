package tarea4;

public class Tarea4 {
	
	public static void main(String[] args) {

		int N = 125;	//definicion variable
		System.out.println("El valor es de: " +N);
		
		int incrementar77 = N+77;//N+77
		System.out.println("Incrementar 77: " +incrementar77);
		
		int decrementar3 = incrementar77-3;//Resultado - 3
		System.out.println("Decrementar 3: " +decrementar3);
		
		int resultado = decrementar3*2; //Resultado*2
		System.out.println("Resultado final: " +resultado);

	}

}
